import { defineComponent, ref, unref, computed } from 'vue'

export default defineComponent({
  name: 'MapApp',

  setup() {
    // –†–µ–∞–∫—Ç–∏–≤–Ω—ã–µ –ø–µ—Ä–µ–º–µ–Ω–Ω—ã–µ –¥–ª—è —Ö—Ä–∞–Ω–µ–Ω–∏—è –∫–æ–æ—Ä–¥–∏–Ω–∞—Ç –º–µ—Ç–∫–∏
    const x = ref(0);
    const y = ref(0);

    const top = computed(() => `${unref(y)}px`);
    const left = computed(() => `${unref(x)}px`);

    /**
     * –û–±—Ä–∞–±–æ—Ç—á–∏–∫ –∫–ª–∏–∫–∞ –ø–æ –∫–∞—Ä—Ç–µ –¥–ª—è —É—Å—Ç–∞–Ω–æ–≤–∫–∏ –∫–æ–æ—Ä–¥–∏–Ω–∞—Ç –º–µ—Ç–∫–∏
     * @param {MouseEvent} event
     */
    function handleClick(event) {
      x.value = event.offsetX;
      y.value = event.offsetY;
    }

    return {
      x,
      y,
      top,
      left,
      handleClick,
    }
  },

  template: /* html */ `
    <div class="map" @click="handleClick">
      <img class="map-image" src="./map.png" alt="Map" draggable="false" />
      <span class="pin" :style="{color: 'red', top: top, left: left}">üìç</span>
    </div>
  `,
})
